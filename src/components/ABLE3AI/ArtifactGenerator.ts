/**
 * Parse screener results into artifact table
 */
function parseScreenerArtifact(response: string, query: string): ArtifactData | null {
  // Try to extract table-like data from response
  const lines = response.split('\n').filter(l => l.includes('|') || l.includes(':'));
  if (lines.length < 3) return null;

  const rows: string[][] = [];
  const headers: string[] = [];

  for (const line of lines) {
    if (line.includes('|')) {
      const cells = line.split('|').map(c => c.trim()).filter(Boolean);
      if (headers.length === 0 && cells.length > 1) {
        headers.push(...cells);
      } else if (cells.length > 1 && !cells.every(c => c.match(/^[-]+$/))) {
        rows.push(cells);
      }
    }
  }

  if (headers.length === 0 || rows.length === 0) return null;

  return {
    id: Date.now().toString(),
    type: 'screener_result',
    title: `🔍 ${query.slice(0, 40)}`,
    timestamp: new Date(),
    source: 'Screener',
    content: { headers, rows },
  };
}

function parseChartArtifact(response: string, query: string): ArtifactData | null {
  // Extract symbol from query
  const symbolMatch = query.match(/\b([A-Z]{2,6}(?:USDT?)?)\b/i);
  const symbol = symbolMatch ? symbolMatch[1].toUpperCase() : 'Unknown';

  // Extract numeric values
  const indicators: Record<string, number> = {};
  const rsiMatch = response.match(/RSI[:\s]*([0-9.]+)/i);
  if (rsiMatch) indicators['RSI'] = parseFloat(rsiMatch[1]);
  const macdMatch = response.match(/MACD[:\s]*([0-9.-]+)/i);
  if (macdMatch) indicators['MACD'] = parseFloat(macdMatch[1]);

  const signals: any[] = [];
  if (response.includes('oversold') || response.includes('ขายมากเกิน')) {
    signals.push({ name: 'Oversold', direction: 'bullish', detail: 'RSI < 30' });
  }
  if (response.includes('overbought') || response.includes('ซื้อมากเกิน')) {
    signals.push({ name: 'Overbought', direction: 'bearish', detail: 'RSI > 70' });
  }
  if (response.includes('golden cross')) {
    signals.push({ name: 'Golden Cross', direction: 'bullish', detail: 'SMA50 > SMA200' });
  }

  return {
    id: Date.now().toString(),
    type: 'chart_analysis',
    title: `📊 ${symbol} Technical Analysis`,
    timestamp: new Date(),
    source: 'Trading Chart',
    content: {
      symbol,
      timeframe: 'Daily',
      indicators: Object.keys(indicators).length > 0 ? indicators : undefined,
      signals: signals.length > 0 ? signals : undefined,
      analysis: extractAnalysisText(response),
    },
  };
}

function parseRelationshipArtifact(response: string, query: string): ArtifactData | null {
  // Detect entities and relationships
  const nodes: any[] = [];
  const connections: any[] = [];

  // Simple entity extraction
  const assetPattern = /\b((?:XAU|BTC|ETH|USD|EUR|GBP|JPY|SPX|DXY|VIX|Gold|Bitcoin|Oil|ทองคำ|น้ำมัน|หุ้น)[A-Z]*)\b/gi;
  const assets = [...new Set(response.match(assetPattern) || [])];
  
  assets.forEach(asset => {
    nodes.push({ label: asset, type: 'asset', category: 'Assets' });
  });

  // Extract event/indicator mentions
  const events = response.match(/(?:Fed|ECB|BOJ|CPI|GDP|NFP|FOMC|PMI|inflation|ดอกเบี้ย|เงินเฟ้อ)[^,.]*/gi) || [];
  events.slice(0, 5).forEach(ev => {
    nodes.push({ label: ev.trim(), type: 'event', category: 'Events & Indicators' });
  });

  if (nodes.length < 2) return null;

  // Create basic connections
  for (let i = 0; i < Math.min(assets.length, 4); i++) {
    for (let j = i + 1; j < Math.min(assets.length, 4); j++) {
      connections.push({
        from: assets[i],
        to: assets[j],
        type: response.includes('inverse') || response.includes('สวนทาง') ? 'negative' : 'positive',
      });
    }
  }

  return {
    id: Date.now().toString(),
    type: 'relationship',
    title: `🔗 ${query.slice(0, 40)}`,
    timestamp: new Date(),
    source: 'AI Analysis',
    content: {
      nodes,
      connections,
      summary: extractAnalysisText(response).slice(0, 300),
    },
  };
}

/* ─── Journal Artifact ─── */

function hasJournalPattern(q: string): boolean {
  return /journal|trading journal|เทรด.*journal|สรุป.*เทรด|ผลการเทรด|win.*rate|p&l|pnl|กำไร.*ขาดทุน|สถิติ.*เทรด|performance|ประสิทธิภาพ|วิเคราะห์.*เทรด|able.*score|psychology|จิตวิทยา|monte.*carlo|r-?multiple|risk.*reward|drawdown|streak|setup.*performance|สรุปผล/i.test(q);
}

function parseJournalArtifact(response: string, query: string): ArtifactData | null {
  // Extract metrics from AI response
  const headers: string[] = [];
  const rows: string[][] = [];
  const groups: { title: string; headers: string[]; rows: string[][] }[] = [];

  // Try to extract table data from response
  const lines = response.split('\n');
  let currentGroup: { title: string; headers: string[]; rows: string[][] } | null = null;

  for (const line of lines) {
    const trimmed = line.trim();
    // Detect section headers
    if (trimmed.startsWith('**') && trimmed.endsWith('**')) {
      if (currentGroup && currentGroup.rows.length > 0) groups.push(currentGroup);
      currentGroup = { title: trimmed.replace(/\*\*/g, ''), headers: [], rows: [] };
      continue;
    }
    // Detect table rows
    if (trimmed.includes('|')) {
      const cells = trimmed.split('|').map(c => c.trim()).filter(Boolean);
      if (cells.length > 1 && !cells.every(c => /^[-:]+$/.test(c))) {
        if (currentGroup) {
          if (currentGroup.headers.length === 0) currentGroup.headers = cells;
          else currentGroup.rows.push(cells);
        } else {
          if (headers.length === 0) headers.push(...cells);
          else rows.push(cells);
        }
      }
    }
    // Detect key-value pairs like "Win Rate: 65%"
    const kvMatch = trimmed.match(/^[•\-]\s*(.+?):\s*(.+)$/);
    if (kvMatch && currentGroup) {
      if (currentGroup.headers.length === 0) currentGroup.headers = ['Metric', 'Value'];
      currentGroup.rows.push([kvMatch[1], kvMatch[2]]);
    }
  }
  if (currentGroup && currentGroup.rows.length > 0) groups.push(currentGroup);

  // If no structured data found, create from numbered items
  if (groups.length === 0 && rows.length === 0) {
    const bulletLines = lines.filter(l => /^[\d]+[.)]\s|^[-•]\s/.test(l.trim()));
    if (bulletLines.length >= 2) {
      groups.push({
        title: '📊 Trading Journal Analysis',
        headers: ['#', 'Detail'],
        rows: bulletLines.map((l, i) => [
          String(i + 1),
          l.replace(/^[\d]+[.)]\s|^[-•]\s/, '').trim()
        ])
      });
    }
  }

  if (groups.length === 0 && rows.length === 0) return null;

  return {
    id: Date.now().toString(),
    type: 'table',
    title: `📔 ${query.slice(0, 50)}`,
    timestamp: new Date(),
    source: 'Trading Journal',
    content: groups.length > 0 ? { groups } : { headers, rows },
    aiResponse: response,
    userQuery: query,
  };
}

/* ─── Helpers ─── */

function hasScreenerPattern(q: string): boolean {
  return /หาหุ้น|screener|scan|top.*gain|oversold|strong.*buy|breakout|volume|สแกน|หุ้นที่น่า|dividend|ปันผล|value.*stock/i.test(q);
}

function hasFinancialPattern(q: string): boolean {
  return /งบการเงิน|financial.*statement|balance.*sheet|income.*statement|cash.*flow|งบดุล|งบกำไร|อัตราส่วน.*การเงิน|financial.*report|ดูงบ|pull.*financial/i.test(q);
}

function hasChartPattern(q: string): boolean {
  return /chart|กราฟ|technical|RSI|MACD|วิเคราะห์.*เทค|analysis.*chart|แนวรับ|แนวต้าน|support|resistance/i.test(q);
}

function hasTableData(text: string): boolean {
  return (text.match(/\|/g) || []).length > 5;
}

function hasTechnicalData(text: string): boolean {
  return /RSI|MACD|SMA|EMA|Bollinger|ATR|volume|signal/i.test(text);
}

function hasRelationshipPattern(text: string): boolean {
  const assetCount = (text.match(/\b(?:XAU|BTC|ETH|USD|EUR|Gold|Bitcoin|Oil|VIX|DXY|SPX|ทองคำ|หุ้น)\b/gi) || []).length;
  return assetCount >= 3;
}

function extractAnalysisText(response: string): string {
  // Get the analysis section
  const analysisIdx = response.indexOf('การวิเคราะห์');
  if (analysisIdx > -1) return response.slice(analysisIdx).slice(0, 500);
  
  const sections = response.split('---');
  return (sections[sections.length - 1] || response).trim().slice(0, 500);
}

function mapMarket(market: string): string {
  const map: Record<string, string> = {
    nasdaq: 'america', nyse: 'america', set: 'thailand', bkk: 'thailand',
    hkex: 'hongkong', tsx: 'canada', lse: 'uk',
  };
  return map[market] || market || 'america';
}
