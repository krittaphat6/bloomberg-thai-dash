/**
 * Detects if AI response should generate an artifact
 */
export function detectArtifactFromResponse(userQuery: string, aiResponse: string): ArtifactData | null {
  const lower = userQuery.toLowerCase();

  // Trading Journal queries
  if (hasJournalPattern(lower)) {
    return parseJournalArtifact(aiResponse, userQuery);
  }

  // Screener / stock results
  if (hasScreenerPattern(lower) && hasTableData(aiResponse)) {
    return parseScreenerArtifact(aiResponse, userQuery);
  }

  // Financial statement queries
  if (hasFinancialPattern(lower)) {
    return null; // Will be generated by fetchFinancialStatement
  }

  // Chart / technical analysis
  if (hasChartPattern(lower) && hasTechnicalData(aiResponse)) {
    return parseChartArtifact(aiResponse, userQuery);
  }

  // Relationship / analysis with multiple entities
  if (hasRelationshipPattern(aiResponse)) {
    return parseRelationshipArtifact(aiResponse, userQuery);
  }

  return null;
}

